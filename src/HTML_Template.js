// push array to page 
HTML_Template = (data) => {

    pageArray = [];

    //Calling positions
    for (let i = 0; i < data.length; i++) {
        const employee = data[i];
        const role = employee.getRole(); 
        if (role === 'Manager') {
            const managerCard = genManaCard(employee);
            pageArray.push(managerCard);
        }

        if (role === 'Engineer') {
            const engineerCard = genEngineCard(employee);

            pageArray.push(engineerCard);
        }

        if (role === 'Intern') {
            const internCard = genInternCard(employee);

            pageArray.push(internCard);
        }
        
    }

    const employeeCards = pageArray.join('')

    const genTeam = genWebpage(employeeCards); 
    return genTeam;

}

